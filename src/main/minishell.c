#include "minishell.h"

int	main(int argc, char **argv, char **envp)
{
	char	*line;

	(void)argc;
	(void)argv;
	line = readline("minishell$ ");
	while (line)
	{
		add_history(line);
		if (line && *line)
			print_tree(parser(line));
		line = readline("minishell$ ");
	}
	exit(0);
	(void)envp;
	return (0);
}
